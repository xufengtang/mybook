package cn.ewb.cq.framework.bean;

public class CheckItemBean {

	private String itemId;
	private String itemCode;
	private String itemBigName;
	private String itemSmallName;
	private String checkContent;
	private String managerFlg;
	private String employeeFlg;
	private String orgFlg;

	public String getItemId() {
		return itemId;
	}

	public void setItemId(String itemId) {
		this.itemId = itemId;
	}

	public String getItemCode() {
		return itemCode;
	}

	public void setItemCode(String itemCode) {
		this.itemCode = itemCode;
	}

	public String getItemBigName() {
		return itemBigName;
	}

	public void setItemBigName(String itemBigName) {
		this.itemBigName = itemBigName;
	}

	public String getItemSmallName() {
		return itemSmallName;
	}

	public void setItemSmallName(String itemSmallName) {
		this.itemSmallName = itemSmallName;
	}

	public String getCheckContent() {
		return checkContent;
	}

	public void setCheckContent(String checkContent) {
		this.checkContent = checkContent;
	}

	public String getManagerFlg() {
		return managerFlg;
	}

	public void setManagerFlg(String managerFlg) {
		this.managerFlg = managerFlg;
	}

	public String getEmployeeFlg() {
		return employeeFlg;
	}

	public void setEmployeeFlg(String employeeFlg) {
		this.employeeFlg = employeeFlg;
	}

	public String getOrgFlg() {
		return orgFlg;
	}

	public void setOrgFlg(String orgFlg) {
		this.orgFlg = orgFlg;
	}

}
